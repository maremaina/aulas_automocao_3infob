#Estrutura de repetição for
#percorrer listas
#lista estrutura de dados composta (armazena diversos valores)
#variavel que possui varios valores

nomes = ["ivan","gael","mariana","isis"]
print(nomes)

#imprime todos os nomes da lista utiçizando range
for i in range (4):
    print(nomes[i])

#imprime todos os nomes usando interador de lista 
for nome in nomes:
    print(nome)